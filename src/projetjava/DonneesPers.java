    private void AffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AffActionPerformed
       DossierMedical h = new DossierMedical();     //button valider
       boolean empty=false;
      if(Ref.getText().equals(""))              //Controle de Saisie (Il peut y avoir des lettres) mais ne peut pas depasser 4 caracteres et doit exister dans le HashMap
        {
            ErrREF.setText("saisir Reference");         
          
            
        }
        else
         if ((Ref.getText()).length() != 4)  
            ErrREF.setText("Reference Doit avoir 4 caracteres ");
            else
             if (LSTDossMed.containsKey(Ref.getText())==false) 
            
            ErrRef.setText("Il n'y a pas de dossier m√©dicale pour cette reference   ");
        else
        {    ErrRef.setText("");
                empty=true;}
         
      if (empty)                                //Afficher le dossier medical selon le reference (Key of the hashmap) 
      {h = LSTDossMed.get(Ref.getText()) ;
       Nom.setText(h.getNom());
       Prenom.setText(h.getPrenom());
       Tel.setText(Long.toString(h.getTelephone()));
       if ("Femme".equals(h.getGenre()))
           FemG.setSelected(true);
       else HomG.setSelected(true);
       DN.setText(h.getJDN()+"/"+h.getMDN()+"/"+h.getADN());}
    }//GEN-LAST:event_AffActionPerformed

